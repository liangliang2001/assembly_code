DATA SEGMENT
INFO1 DB 0DH,0AH,'INPUT STRING: $'
INFO2 DB 0DH,0AH,'OUTPUT STRING: $'
BUFA DB 81
     DB ?
     DB 80 DUP(?)
BUFB DB 81 DUP(?)
DATA ENDS
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
START:
    MOV AX,DATA
    MOV DS,AX

    LEA DX,INFO1
    MOV AH,9
    INT 21H
    LEA DX,BUFA
    MOV AH,10
    INT 21H

    LEA SI,BUFA+1
    MOV CH,0
    MOV CL,[SI]
    ADD SI,CX
    LEA DI,BUFB
NEXT:
    MOV AL,[SI]
    MOV [DI],AL
    DEC SI
    INC DI
    LOOP NEXT
    MOV BYTE PTR [DI],'$'
    LEA DX,INFO2
    MOV AH,9
    INT 21H
    LEA DX,BUFB
    MOV AH,9
    INT 21H

    MOV AH,4CH
    INT 21H
CODE ENDS
END START