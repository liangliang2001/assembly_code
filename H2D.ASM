CODE SEGMENT
    ASSUME CS:CODE
START:
    CALL HIB
    CALL CRLF
    CALL BID
    CALL CRLF
    JMP START
HIB PROC NEAR
    MOV BX,0
REP1: MOV AH,1
    INT 21H
    SUB AL,30H
    JL EXIT
    CMP AL,10
    JL ADD1
    SUB AL,7
    CMP AL,0AH
    JL EXIT
    CMP AL,10H
    JGE EXIT
ADD1: MOV CL,4
    SHL BX,CL 
    MOV AH,0
    ADD BX,AX
    JMP REP1
EXIT:RET
HIB ENDP
BID PROC NEAR
    MOV CX,10000
    CALL DDIV
    MOV CX,1000
    CALL DDIV
    MOV CX,100
    CALL DDIV
    MOV CX,10
    CALL DDIV
    MOV CX,1
    CALL DDIV
    RET
BID ENDP
DDIV PROC NEAR
    MOV AX,BX
    MOV DX,0
    DIV CX
    MOV BX,DX
    MOV DL,AL
    ADD DL,30H
    MOV AH,2
    INT 21H
    RET
DDIV ENDP
CRLF PROC NEAR
    MOV DL,0DH
    MOV AH,2
    INT 21H
    MOV DL,0AH
    MOV AH,2
    INT 21H
    RET
CRLF ENDP
CODE ENDS
    END START